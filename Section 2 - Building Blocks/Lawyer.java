// building a public class
public class Lawyer {
    String name;
    String field;
    String barNumber;

    // building public getter methods
    public String getName() {
        return name;
    }

    public String getField() {
        return field;
    }

    public String getBarNumber() {
        return barNumber;
    }

    // building public setters methods
    public void setName(String name) {
        name = name;
    }

    public void setField(String field) {
        field = field;
    }

    public void setBarNumber(String barNumber) {
        barNumber = barNumber;
    }

}